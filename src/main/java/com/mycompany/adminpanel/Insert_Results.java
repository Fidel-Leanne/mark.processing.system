    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         Double results[]= new Double[5];
         
         results[0]= Double.valueOf(semester_mark.getText());
         results[1]=Double.valueOf(exam_Mark.getText());
         results[2]=(results[0]+results[1])/2;
         String FinalMark=String.format("%.0f", results[2]);
         final_Mark.setText(FinalMark);
        String studentNo = studentnum.getText();
        String Name = name.getText();
        String Faculty = (String) faculty.getSelectedItem();
        String courseCode = coursecode.getText();
        String semesterMark = semester_mark.getText();
        String examMark = exam_Mark.getText();
        String finalMark = final_Mark.getText();
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
        String[]toadd={Name,Faculty,studentNo,courseCode,semesterMark,examMark,finalMark};
        dtm.addRow(toadd);
        studentnum.setText("");
        name.setText("");
        faculty.setSelectedItem("");
        coursecode.setText("");
        semester_mark.setText("");
        exam_Mark.setText("");
        final_Mark.setText("");

        
    }//GEN-LAST:event_jButton1ActionPerformed

