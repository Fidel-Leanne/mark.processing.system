    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
        int index=jTable1.getSelectedRow();
        studentnum.setText(dtm.getValueAt(index,0).toString());
        name.setText(dtm.getValueAt(index,1).toString());
        faculty.setSelectedItem(dtm.getValueAt(index,2).toString());
        coursecode.setText(dtm.getValueAt(index,3).toString());
        semester_mark.setText(dtm.getValueAt(index,4).toString());
        exam_Mark.setText(dtm.getValueAt(index,5).toString());
        final_Mark.setText(dtm.getValueAt(index,6).toString());
        
    }//GEN-LAST:event_jTable1MouseClicked

