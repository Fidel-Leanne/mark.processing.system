    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String studentNo = studentnum.getText();
        String Name = name.getText();
        String Faculty = (String) faculty.getSelectedItem();
        String courseCode = coursecode.getText();
        String semesterMark = semester_mark.getText();
        String examMark = exam_Mark.getText();
        String finalMark = final_Mark.getText();
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
        int index=jTable1.getSelectedRow();
        dtm.setValueAt(studentNo, index, 0);
        dtm.setValueAt(Name, index, 1);
        dtm.setValueAt(Faculty, index, 2);
        dtm.setValueAt(courseCode, index, 3);
        dtm.setValueAt(semesterMark, index, 4);
        dtm.setValueAt(examMark, index, 5);
        dtm.setValueAt(finalMark, index, 6);
        JOptionPane.showMessageDialog(this,"Update successfull!");
        
    }//GEN-LAST:event_jButton2ActionPerformed

